package com.apisoccer.domain;

public class History {
	private Integer id;
	private String cam;
	private String sub;
	private String ter;
	private String cuar;
	private String torn;
	private String comp;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCam() {
		return cam;
	}
	public void setCam(String cam) {
		this.cam = cam;
	}
	public String getSub() {
		return sub;
	}
	public void setSub(String sub) {
		this.sub = sub;
	}
	public String getTer() {
		return ter;
	}
	public void setTer(String ter) {
		this.ter = ter;
	}
	public String getCuar() {
		return cuar;
	}
	public void setCuar(String cuar) {
		this.cuar = cuar;
	}
	public String getTorn() {
		return torn;
	}
	public void setTorn(String torn) {
		this.torn = torn;
	}
	public String getComp() {
		return comp;
	}
	public void setComp(String comp) {
		this.comp = comp;
	}
	
	

}
